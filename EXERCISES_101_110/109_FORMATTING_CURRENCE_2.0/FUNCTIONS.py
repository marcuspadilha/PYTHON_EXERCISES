def half(num1):
    return f"The half of the value is R$ {str(f'{num1/2:.2f}').replace('.',',')}."

def double(num2):
    return f"The double of the value is R$ {str(f'{num2*2:.2f}').replace('.',',')}."

def adding10(num3):
    return f"Add 10% in the value we get R$ {str(f'{num3*1.1:.2f}').replace('.',',')}."



